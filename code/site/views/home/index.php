			<h1 style="font-weight: 400; font-size: 2.2em; letter-spacing: .03em; font-family: 'Unna', serif;">Grown up at 35?</h1>						

			<p style="font-size: 1.3em; line-height: 1.6em; text-align: justify; margin-bottom: 25px;">
				<span class="pull-left" style="font-size: 5.6em; padding-top: .29em; padding-right: 5px;">A</span>n interesting development in the later-in-life Parenting Sweepstakes (to be filed under <em>Are We <strike>Ever</strike> Never Done With Adolescence, not even when we&rsquo;re on Medicare?</em>): &nbsp;Three of the males in my extended family -- a nephew, a nephew-in-law and my son -- recently volunteered that something had happened this month that made them feel &ldquo;like an adult.&rdquo; &nbsp;Two of these young 30-somethings are already married with children and a house mortgage and the third just learned he and his wife have a child on the way (and you might think that would have done it, but it didn&rsquo;t). &nbsp;Instead, two of the triggers were quite simple -- my son&rsquo;s new winter coat (so he no longer feels like the college kid he was when he got his last one), &nbsp;and my nephew-in-law&rsquo;s first-ever personalized stationery of his own. &nbsp;The third apparently life-changing event, my 36 year-old nephew&rsquo;s, is more obvious: he moved his year-old architecture business out of his mother&rsquo;s basement into an office of his own and he now feels, as the others said they did, &ldquo;grown up.&rdquo;
			</p>			
			<blockquote class="pull-right col-sm-3" style=" border: none; padding: 0 0 0 30px; margin: 0; font-size: 2em; font-weight: 300; font-family: 'Lancelot', cursive;">
				Two of these young 30-somethings are already married with children.
			</blockquote>
			<p style="font-size: 1.3em; line-height: 1.6em; text-align: justify; margin-bottom: 25px;">
				I am wondering what to make of these serendipitous proclamations of maturity. &nbsp;At the least they suggest there&rsquo;s something in these guys&rsquo; cultural environment that supports the feeling they maybe <em>should</em> be grown-ups now. I guess that&rsquo;s good news, albeit a little late from my husband&rsquo;s perspective (which involves the (of course unspoken) &ldquo;Why at your age, we had. . .&rdquo; &nbsp;and words like &ldquo;career path&rdquo;). &nbsp;It could be, however, that there was something in <em>our</em> cultural -- and parenting -- environment that posited that they didn&rsquo;t have to before now, and truth to tell, that&rsquo;s what&rsquo;s bugging me. &nbsp;These guys are good guys -- responsible, hard-working, interesting, engaged with life and ideas; they&rsquo;re great. &nbsp;That they&rsquo;ve had to wait to 35 to feel grown up strikes me as a little sad. &nbsp;I know developmental psychologists believe adolescence today persists well into one&rsquo;s 20&rsquo;s because of a number of factors including our educational system, the economy, and our don&rsquo;t-let-them-fail parenting style. &nbsp;But 35? &nbsp;Have we been too critical, over-involved, overly generous? &nbsp;Even as I ask my son to mix me a Sidecar cocktail (I mean, really, how much more adult can you get?) am I infantilizing him just because I&rsquo;m still looking for ways to show him how much I love him? &nbsp;Could his proclamation be a gentle &ldquo;Hands Off, Mom&rdquo;?
			</p>
			
			<p style="font-size: 1.3em; line-height: 1.6em; text-align: justify; margin-bottom: 25px;">
				I can hear my daughter -- and the shrink -- saying &ldquo;It&rsquo;s not about you, Mom/Ann.&rdquo; &nbsp;And of course it&rsquo;s not. &nbsp;In fact, the more I think about it, the more I remember feeling 35 myself and thinking it was about the most wonderful age there was -- old enough to know better (whatever) and young enough to enjoy it. &nbsp;And that&rsquo;s probably all they were saying, these boys in my family; they had arrived at a certain place in their lives, they felt good, and they equated the feeling with being grown up. &nbsp;That&rsquo;s cool. &nbsp;
			</p>
			<p style="font-size: 1.3em; line-height: 1.6em; text-align: justify; margin-bottom: 25px;">
				Now, if we could just do the same for 65. &nbsp;<em>That</em> would be Really Cool!
			</p>


